# 1. Arithmetic Operators: used with numeric values to perform common 
# mathematical operations

a = 10
b = 5
# Addition
print('a + b =',a + b)
# Subtraction
print('a - b =', a - b)
# Multiplication
print('a * b =', a * b)
# Division
print('a / b =', a / b)
# Modulus
print('a "%" b =', a % b)
# Exponentiation
print('a ** b =', a ** b)
# Floor division
print('a // b =', a // b,'\n')


